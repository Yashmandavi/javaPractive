package Acm2;

public class Assignmentoperators {
    int x = 100;
    public static void main(String[] args) {
        int x = 20;
        x+=3;
        System.out.println(x);
        x-=3;
        System.out.println(x);
        x= x+3;
        System.out.println(x);
        x = x-3;
        System.out.println(x);
        x%=3;
        System.out.println(x);
        x=20;
        x&=3;
        System.out.println(x);



    }
}
